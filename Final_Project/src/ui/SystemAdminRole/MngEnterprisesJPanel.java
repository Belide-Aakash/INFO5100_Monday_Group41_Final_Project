    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Network slectedNetwork = (Network) cBoxNetworks.getSelectedItem();
        Enterprise.EnterpriseType selectedEnterpriseType = (Enterprise.EnterpriseType) cBoxEnterpriseTypes.getSelectedItem();
        
        if (slectedNetwork == null) {
            JOptionPane.showMessageDialog(this, "Select a valid network from the drop down.", "Network Selection Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (selectedEnterpriseType == null) {
            JOptionPane.showMessageDialog(this, "Select a valid enterprise type from the drop down.", "Enterprise Type Selection Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String newEnterpriseName = txtEnterpriseName.getText();
        
        if (newEnterpriseName.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please type a valid enterprise name.", "Enterprise Creation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Enterprise newEnterprise = slectedNetwork.getEnterpriseDirectory().createAndAddEnterprise(newEnterpriseName, selectedEnterpriseType);
        
        JOptionPane.showMessageDialog(this, "Enterprise created successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        populateTable();
    }//GEN-LAST:event_btnAddActionPerformed

    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) networkEnterpriseTable.getModel();
        model.setRowCount(0);
        
        for (Network network : this.ecosystemBusiness.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                Object[] row = new Object[3];
                row[0] = network.getNetworkName();
                row[1] = enterprise.getEnterpriseType().getEnterpriseValue();
                row[2] = enterprise.getOrgName();

                model.addRow(row);
            }
        }
    }
    
    public void populateComboboxes() {
        cBoxNetworks.removeAllItems();
        cBoxEnterpriseTypes.removeAllItems();

        for (Network network : this.ecosystemBusiness.getNetworkList()) {
            cBoxNetworks.addItem(network);
        }

        for (Enterprise.EnterpriseType enterpriseType : Enterprise.EnterpriseType.values()) {
            cBoxEnterpriseTypes.addItem(enterpriseType);
        }
    }

