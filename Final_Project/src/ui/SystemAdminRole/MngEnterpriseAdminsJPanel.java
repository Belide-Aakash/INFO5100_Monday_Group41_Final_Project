    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) networkEnterpriseTable.getModel();
        model.setRowCount(0);
        
        for (Network network : this.ecosystemBusiness.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (UserAccount userAccount : enterprise.getUserAccountDir().getUserAccountList()) {
                    Object[] row = new Object[5];
                    row[0] = network.getNetworkName();
                    row[1] = enterprise.getEnterpriseType().getEnterpriseValue();
                    row[2] = enterprise.getOrgName();
                    row[3] = userAccount.getUsername();
                    row[4] = userAccount.getUserRole();

                    model.addRow(row);
                }
            }
        }
    }
    
    public void populateNetworkCombobox() {
        cBoxNetworks.removeAllItems();

        for (Network network : this.ecosystemBusiness.getNetworkList()) {
            cBoxNetworks.addItem(network);
        }
    }
    
    private void populateEnterpriseCombobox(Network network){
        cBoxEnterpriseNames.removeAllItems();
        
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
            cBoxEnterpriseNames.addItem(enterprise);
        }
    }
    
    private void populateEnterpriseRolesCombobox(Enterprise enterprise){
        cBoxEnterpriseUserRole.removeAllItems();
        
        for (Role userRole : enterprise.getSupportedRole()){
            cBoxEnterpriseUserRole.addItem(userRole);
        }
    }

