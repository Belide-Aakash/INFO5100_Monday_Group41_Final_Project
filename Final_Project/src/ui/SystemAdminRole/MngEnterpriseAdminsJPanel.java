    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Network slectedNetwork = (Network) cBoxNetworks.getSelectedItem();
        Enterprise selectedEnterprise = (Enterprise) cBoxEnterpriseNames.getSelectedItem();
        Role selectedEnterpriseRole = (Role) cBoxEnterpriseUserRole.getSelectedItem();

        if (slectedNetwork == null) {
            JOptionPane.showMessageDialog(this, "Select a valid network from the drop down.", "Network Selection Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (selectedEnterprise == null) {
            JOptionPane.showMessageDialog(this, "Select a valid enterprise from the drop down.", "Enterprise Selection Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (selectedEnterpriseRole == null) {
            JOptionPane.showMessageDialog(this, "Select a valid role from the drop down.", "Role Selection Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String newUsername = txtUsername.getText();
        String newPassword = String.valueOf(txtPassword.getPassword());
        String newName = txtName.getText();

        if (newUsername.isBlank() || newPassword.isBlank() || newName.isBlank()) {
            JOptionPane.showMessageDialog(this, "Username, password and name cannot be empty.", "Enterprise Creation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Person newPerson = selectedEnterprise.getPersonDir().createPerson(newName);
        
        UserAccount newUserAccount = selectedEnterprise.getUserAccountDir().createUserAccount(newUsername, newPassword, newPerson, selectedEnterpriseRole) ;

        JOptionPane.showMessageDialog(this, "Enterprise admin created successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        populateTable();
    }//GEN-LAST:event_btnAddActionPerformed

