        public void populateReqs() {
        DefaultTableModel model = (DefaultTableModel) tblSupplierReqs.getModel();
        model.setRowCount(0);

        for (WorkRequest workReq : this.userAccount.getWorkQueue().getWorkQueue()) {
            Object row[] = new Object[5];
            row[0] = workReq.getSender();
            row[1] = workReq.getReceiver();
            row[2] = workReq.getRequestedDate();
            row[3] = workReq;
            row[4] = workReq.getWorkStatus();
            model.addRow(row);
        }
    }

