    private void btnViewSupReqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewSupReqActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblSupplierReqs.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a request first.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // This is an advertisement request from advertisement workqueue catalog
        WorkRequest selectedReq = (WorkRequest) tblSupplierReqs.getValueAt(selectedRowIndex, 3);

        if (selectedReq.getWorkStatus().equalsIgnoreCase("Pending")) {
            JOptionPane.showMessageDialog(this, "You can view the request once status is completed.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        ViewSupplierReqJPanel viewSupplierReqJPanel = new ViewSupplierReqJPanel(this.userProcessContainer, selectedReq);
        this.userProcessContainer.add("ViewSupplierReqJPanel",viewSupplierReqJPanel);
        CardLayout layout=(CardLayout)this.userProcessContainer.getLayout();
        layout.next(this.userProcessContainer);
    }//GEN-LAST:event_btnViewSupReqActionPerformed

