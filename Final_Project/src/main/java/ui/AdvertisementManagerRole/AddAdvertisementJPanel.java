    public void setupSpinner() {
        // Get the current LocalDateTime
        LocalDateTime now = LocalDateTime.now();

        // Convert LocalDateTime to Date
        Date nowDate = java.sql.Timestamp.valueOf(now);

        // Configure the SpinnerDateModel with the current date as the lower bound
        SpinnerDateModel dateModel = new SpinnerDateModel();
        dateModel.setStart(nowDate); // Set minimum date to current date
        spinExpDatetime.setModel(dateModel);

        // Add a custom editor to show both date and time in the spinner
        JSpinner.DateEditor dateEditor = new JSpinner.DateEditor(spinExpDatetime, "yyyy-MM-dd HH:mm");
        spinExpDatetime.setEditor(dateEditor);
    }
    
    // Method to retrieve the selected date as LocalDateTime
    public LocalDateTime getSelectedDateTime() {
        Date selectedDate = (Date) spinExpDatetime.getValue();
        return selectedDate.toInstant().atZone(java.time.ZoneId.systemDefault()).toLocalDateTime();
    }

    // Method to reset the spinner to the current date/time
    public void resetSpinner() {
        LocalDateTime now = LocalDateTime.now();
        Date nowDate = java.sql.Timestamp.valueOf(now);
        spinExpDatetime.setValue(nowDate);
    }

