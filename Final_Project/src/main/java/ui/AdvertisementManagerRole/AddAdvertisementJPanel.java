    private void btnAddAdvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddAdvActionPerformed
        // TODO add your handling code here:
        String advName = txtAdvName.getText();
        
        if (txtAdvName.getText().isBlank() || txtLatitude.getText().isBlank() || txtLongitude.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "All fields all mandatory.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (advImage == null) {
            JOptionPane.showMessageDialog(this, "Please attach an image for advertisement.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        LocalDateTime advExpDatetime;
        float advLat;
        float advLongi;
        
        try {
            advExpDatetime = getSelectedDateTime();
            
            if (advExpDatetime.isBefore(LocalDateTime.now())) {
                JOptionPane.showMessageDialog(this, "Expiry date must be in the future.", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid date.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            advLat = Float.parseFloat(txtLatitude.getText());
            advLongi = Float.parseFloat(txtLongitude.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter a latitude and longitude.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        this.enterprise.getAdvertisementCatalog().addAdvertisement(advName, advExpDatetime, advImage, advLat, advLongi);
        
        JOptionPane.showMessageDialog(this, "Advertisement created successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
        txtAdvName.setText("");
        resetSpinner();
        txtLatitude.setText("");
        txtLongitude.setText("");
        advImage = null;
        imageAdvertisement.setIcon(advImage);
    }//GEN-LAST:event_btnAddAdvActionPerformed

    private void btnAttachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttachActionPerformed
        // TODO add your handling code here:
        int returnVal = fileChooser.showOpenDialog(this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            URL url;

            try {
                url = file.toURI().toURL();
                advImage = new ImageIcon(url);
                advImage = new ImageIcon(advImage.getImage().getScaledInstance(150, 350, Image.SCALE_SMOOTH));

                imageAdvertisement.setIcon(advImage);
            } catch (MalformedURLException ex) {
                JOptionPane.showMessageDialog(this, "There was an error in loading your image. Please try again.", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
    }//GEN-LAST:event_btnAttachActionPerformed

